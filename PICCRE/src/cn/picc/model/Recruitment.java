package cn.picc.model;

import java.math.BigDecimal;
import java.util.Date;

public class Recruitment {
	
    private BigDecimal pkAppDem; //应聘需求主键

    private String demCode; //应聘需求编号

    private Date demCreareTime; //创建时间

    private Date demPublishTime; //发布时间

    private Date demEndTime; //截止时间

    private String demContent; //内容

    private String status; //状态

    private Date modifyTime; //上传/修改时间

    private String demName; //需求名称
    
    private String appContent;

    public String getAppContent() {
		return appContent;
	}

	public void setAppContent(String appContent) {
		this.appContent = appContent;
	}

	public BigDecimal getPkAppDem() {
        return pkAppDem;
    }

    public void setPkAppDem(BigDecimal pkAppDem) {
        this.pkAppDem = pkAppDem;
    }

    public String getDemCode() {
        return demCode;
    }

    public void setDemCode(String demCode) {
        this.demCode = demCode;
    }

    public Date getDemCreareTime() {
        return demCreareTime;
    }

    public void setDemCreareTime(Date demCreareTime) {
        this.demCreareTime = demCreareTime;
    }

    public Date getDemPublishTime() {
        return demPublishTime;
    }

    public void setDemPublishTime(Date demPublishTime) {
        this.demPublishTime = demPublishTime;
    }

    public Date getDemEndTime() {
        return demEndTime;
    }

    public void setDemEndTime(Date demEndTime) {
        this.demEndTime = demEndTime;
    }

    public String getDemContent() {
        return demContent;
    }

    public void setDemContent(String demContent) {
        this.demContent = demContent;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public Date getModifyTime() {
        return modifyTime;
    }

    public void setModifyTime(Date modifyTime) {
        this.modifyTime = modifyTime;
    }

    public String getDemName() {
        return demName;
    }

    public void setDemName(String demName) {
        this.demName = demName;
    }
}