package cn.picc.model;

import java.math.BigDecimal;
import java.util.Date;

public class Experience {
    private BigDecimal pkExp; //履历主键

    private BigDecimal pkEmp; //员工主键

    private Date begainTime; //起始时间

    private Date endTime; //截止时间

    private String workPlace; //工作单位

    private String duty; //担任职务

    private String dept; //所在部门

    private String post; //岗位

    private Date modifyTime; //修改时间

    public BigDecimal getPkExp() {
        return pkExp;
    }

    public void setPkExp(BigDecimal pkExp) {
        this.pkExp = pkExp;
    }

    public BigDecimal getPkEmp() {
        return pkEmp;
    }

    public void setPkEmp(BigDecimal pkEmp) {
        this.pkEmp = pkEmp;
    }

    public Date getBegainTime() {
        return begainTime;
    }

    public void setBegainTime(Date begainTime) {
        this.begainTime = begainTime;
    }

    public Date getEndTime() {
        return endTime;
    }

    public void setEndTime(Date endTime) {
        this.endTime = endTime;
    }

    public String getWorkPlace() {
        return workPlace;
    }

    public void setWorkPlace(String workPlace) {
        this.workPlace = workPlace;
    }

    public String getDuty() {
        return duty;
    }

    public void setDuty(String duty) {
        this.duty = duty;
    }

    public String getDept() {
        return dept;
    }

    public void setDept(String dept) {
        this.dept = dept;
    }

    public String getPost() {
        return post;
    }

    public void setPost(String post) {
        this.post = post;
    }

    public Date getModifyTime() {
        return modifyTime;
    }

    public void setModifyTime(Date modifyTime) {
        this.modifyTime = modifyTime;
    }
}