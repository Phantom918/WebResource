package cn.hnne.hs.model.privilege;

import java.io.Serializable;

public class Privilege implements Serializable{
	
	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	private Integer privilegeId;
	
	private String privilegeName;
	
	private Integer privilegeCode;
	
	private String privilegeAction;
	
	private Module module;

	public Integer getPrivilegeId() {
		return privilegeId;
	}

	public void setPrivilegeId(Integer privilegeId) {
		this.privilegeId = privilegeId;
	}

	public String getPrivilegeName() {
		return privilegeName;
	}

	public void setPrivilegeName(String privilegeName) {
		this.privilegeName = privilegeName;
	}

	public Integer getPrivilegeCode() {
		return privilegeCode;
	}

	public void setPrivilegeCode(Integer privilegeCode) {
		this.privilegeCode = privilegeCode;
	}

	public String getPrivilegeAction() {
		return privilegeAction;
	}

	public void setPrivilegeAction(String privilegeAction) {
		this.privilegeAction = privilegeAction;
	}

	public Module getModule() {
		return module;
	}

	public void setModule(Module module) {
		this.module = module;
	}
	
}
